function populateSelects() {
    const etfNames = Object.keys(etfData);
    const select1 = document.getElementById('etfSelect1');
    const select2 = document.getElementById('etfSelect2');
    select1.innerHTML = select2.innerHTML = '';
    etfNames.forEach(name => {
        select1.innerHTML += `<option value="${name}">${name}</option>`;
        select2.innerHTML += `<option value="${name}">${name}</option>`;
    });
}

function compareETFs() {
    const etf1 = document.getElementById('etfSelect1').value;
    const etf2 = document.getElementById('etfSelect2').value;
    const holdings1 = etfData[etf1];
    const holdings2 = etfData[etf2];

    const overlap = [];
    const totalHoldings = {};

    holdings1.forEach(stock => {
        totalHoldings[stock.code] = { weight1: stock.weight, weight2: 0 };
    });

    holdings2.forEach(stock => {
        if (totalHoldings[stock.code]) {
            totalHoldings[stock.code].weight2 = stock.weight;
            overlap.push(stock.code);
        } else {
            totalHoldings[stock.code] = { weight1: 0, weight2: stock.weight };
        }
    });

    overlap.sort((a, b) => {
        return totalHoldings[b].weight1 - totalHoldings[a].weight1;
    });

    displayResult(overlap, totalHoldings, etf1, etf2);
}

function displayResult(overlap, totalHoldings, etf1, etf2) {
    let etf1Stocks = Object.keys(totalHoldings).filter(stock =>     totalHoldings[stock].weight1 > 0).length;
    let etf2Stocks = Object.keys(totalHoldings).filter(stock =>     totalHoldings[stock].weight2 > 0).length;
    // 計算重疊成分股在兩檔ETF的持股比例總和
    let overlapWeight1 = 0;
    let overlapWeight2 = 0;
    overlap.forEach(stock => {
     overlapWeight1 += totalHoldings[stock].weight1 || 0;
     overlapWeight2 += totalHoldings[stock].weight2 || 0;
});

// 取平均並保留兩位小數
const overlapRate = ((overlapWeight1 + overlapWeight2) / 2).toFixed(2);

// 產生粗體文案（HTML格式）
const overlapRateLine = `<strong>平均成分股重疊率：${overlapRate}%</strong>`;
    let resultHtml = `<h2>比較結果</h2><p>${etf1} 成分股共 ${etf1Stocks} 檔、${etf2} 成分股共 ${etf2Stocks} 檔，重疊的成分股共 ${overlap.length} 檔</p>`;
    resultHtml += `<p>${overlapRateLine}`;
    resultHtml += `<table><tr><th>股票</th><th>${etf1} 持股比例</th><th>${etf2} 持股比例</th></tr>`;
    let etf1TotalWeight = 0;
    let etf2TotalWeight = 0;
    overlap.forEach(stock => {
        etf1TotalWeight += totalHoldings[stock].weight1;
        etf2TotalWeight += totalHoldings[stock].weight2;
        resultHtml += `<tr><td>${stock}</td><td>${totalHoldings[stock].weight1.toFixed(2)}</td><td>${totalHoldings[stock].weight2.toFixed(2)}</td></tr>`;
    });
    resultHtml += `<tr class="total-row"><td>總計</td><td>${etf1TotalWeight.toFixed(2)}</td><td>${etf2TotalWeight.toFixed(2)}</td></tr>`;
    resultHtml += `</table>`;
    document.getElementById('result').innerHTML = resultHtml;
}

